public class Simple_calculator{
    public static void main(String[] args) {
        Float a = 10.00f;
        Float b = 5.00f;
        System.out.print("Isi variable a adalah ");
        System.out.println(a);
        System.out.print("Isi variabel b adalah ");
        System.out.println(b);
        System.out.print("Hasil penjumlahan variabel a dan b adalah ");
        System.out.println(a+b);
        System.out.print("Hasil pengurangan variabel a dan b adalah ");
        System.out.println(a-b);
        System.out.print("Hasil perkalian variabel a dan b adalah ");
        System.out.println(a*b);
        System.out.print("Hasil pembagian variabel a dan b adalah ");
        System.out.println(a/b);
    }
}